package movie;

public class MovieVO {

	private String movie_id;
	private String movie_name;
	private String movie_director;
	private String movie_runt;
	private String movie_actor;
	private String movie_open;
	private String grade_id;
	private String genre_id;
	
	
	
	public String getMovie_id() {
		return movie_id;
	}
	public void setMovie_id(String movie_id) {
		this.movie_id = movie_id;
	}
	public String getMovie_name() {
		return movie_name;
	}
	public void setMovie_name(String movie_name) {
		this.movie_name = movie_name;
	}
	public String getMovie_director() {
		return movie_director;
	}
	public void setMovie_director(String movie_director) {
		this.movie_director = movie_director;
	}
	public String getMovie_runt() {
		return movie_runt;
	}
	public void setMovie_runt(String movie_runt) {
		this.movie_runt = movie_runt;
	}
	public String getMovie_actor() {
		return movie_actor;
	}
	public void setMovie_actor(String movie_actor) {
		this.movie_actor = movie_actor;
	}
	public String getMovie_open() {
		return movie_open;
	}
	public void setMovie_open(String movie_open) {
		this.movie_open = movie_open;
	}
	public String getGrade_id() {
		return grade_id;
	}
	public void setGrade_id(String grade_id) {
		this.grade_id = grade_id;
	}
	public String getGenre_id() {
		return genre_id;
	}
	public void setGenre_id(String genre_id) {
		this.genre_id = genre_id;
	}
	
	
	
}
